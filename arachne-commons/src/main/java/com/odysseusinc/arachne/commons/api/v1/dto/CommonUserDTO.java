package com.odysseusinc.arachne.commons.api.v1.dto;

import java.util.Date;

public class CommonUserDTO {
    private String id;
    private String password;
    private String email;
    private String firstname;
    private String middlename;
    private String lastname;
    private String username;
    private Boolean emailConfirmed;
    private Boolean enabled;
    private Date created;
    private Date updated;
    private CommonProfessionalTypeDTO professionalType;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getMiddlename() {
        return middlename;
    }

    public void setMiddlename(String middlename) {
        this.middlename = middlename;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getUsername() {

        return username;
    }

    public void setUsername(String username) {

        this.username = username;
    }

    public Boolean getEmailConfirmed() {
        return emailConfirmed;
    }

    public void setEmailConfirmed(Boolean emailConfirmed) {
        this.emailConfirmed = emailConfirmed;
    }

    public Boolean getEnabled() {
        return enabled;
    }

    public void setEnabled(Boolean enabled) {
        this.enabled = enabled;
    }

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }

    public Date getUpdated() {
        return updated;
    }

    public void setUpdated(Date updated) {
        this.updated = updated;
    }

    public CommonProfessionalTypeDTO getProfessionalType() {
        return professionalType;
    }

    public void setProfessionalType(CommonProfessionalTypeDTO professionalType) {
        this.professionalType = professionalType;
    }

    public String getFullName() {
        return getFirstname() + " " + (getMiddlename() != null ? getMiddlename() : "") + " " + getLastname();
    }
}
